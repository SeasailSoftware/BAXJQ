        public int GetShiftIdByDateTime(DateTime dt)
        {
            int shiftId = -1;
            switch (CType)
            {
                case 0:
                    DayOfWeek dow = dt.DayOfWeek;
                    shiftId = GetShiftByDayOfWeek(dow);
                    break;
                case 1:
                    int day = dt.Day;
                    shiftId = GetShiftByDay(day);
                    break;
            }
            return shiftId;
        }

